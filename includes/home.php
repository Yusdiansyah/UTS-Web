        <p style="font-size: 18px;">
            Faker, atau Lee Sang-hyeok, dianggap sebagai legenda dalam dunia League of Legends karena pencapaiannya yang luar biasa, keterampilan individu yang hebat, serta dampaknya yang besar terhadap esports dan komunitas League of Legends secara global.
            <br><br>
        </p>
        <h3>
            <br>
            Debut dan Kesuksesan Awal (2013)
        </h3>
        <p>
            <p style="font-size: 18px;">
                Faker memulai karier profesionalnya pada tahun 2013 bersama tim SK Telecom T1, yang pada saat itu juga masih tim yang baru di ranah kompetisi League of Legends. Di musim pertamanya, Faker berhasil memimpin timnya memenangkan Kejuaraan Dunia League of Legends di Los Angeles. <br>Di turnamen tersebut, Faker tampil mengesankan dan menunjukkan keterampilan luar biasa, termasuk saat menggunakan champion Zed dalam pertandingan ikonik melawan Ryu dari tim KT Rolster. Duel antara kedua pemain ini dikenang sebagai salah satu momen terbaik dalam sejarah League of Legends dan membantu memperkenalkan Faker sebagai pemain berbakat yang memiliki kemampuan mekanik luar biasa. Kemenangan ini tidak hanya melambungkan nama Faker, tetapi juga membuat SK Telecom T1 menjadi tim yang disegani.
            </p>
        </div>
        <div>
            <h3>
                <br>
                Dominasi dan Gelar Dunia Kedua (2015)
            </h3>
            <p style="font-size: 18px;">
                Setelah debut gemilang, Faker dan timnya mengalami beberapa tantangan dan sempat mengalami penurunan performa. Namun, pada tahun 2015, Faker kembali membawa SK Telecom T1 mendominasi kancah kompetitif. Dengan adaptasi yang cepat terhadap meta permainan baru dan peningkatan keterampilan, Faker berhasil membawa timnya meraih gelar juara dunia untuk kedua kalinya. Di Kejuaraan Dunia 2015, Keberhasilan ini menunjukkan konsistensi Faker di level tertinggi dan mengukuhkannya sebagai pemain yang sulit ditandingi.
            </p>
        </div>
        <div>
            <h3>
                <br>
                Masa Keemasan dan Gelar Dunia Ketiga (2016)
            </h3>
            <p style="font-size: 18px;">
                Tahun 2016 menjadi salah satu puncak kejayaan bagi Faker. Ia berhasil memenangkan Kejuaraan Dunia untuk ketiga kalinya, menjadikannya satu-satunya pemain dalam sejarah League of Legends yang mencapai tiga gelar dunia. Sepanjang musim, Faker dan timnya menunjukkan kemampuan adaptasi yang luar biasa di tengah perubahan meta dan strategi kompetisi. Kemenangan ini membuat SK Telecom T1 semakin diakui sebagai tim terkuat di dunia, dan Faker menjadi simbol keunggulan di dunia League of Legends.        
            </p>
        </div>
