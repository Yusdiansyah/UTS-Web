            <h3>
                Tantangan dan Konsistensi di Tengah Kompetisi Ketat (2017-2019)
            </h3>
            <p style="font-size: 18px;">
                Setelah meraih gelar dunia ketiganya, Faker menghadapi periode yang penuh tantangan, baik dari segi performa tim maupun meningkatnya persaingan dari tim-tim internasional. Tahun 2017 menjadi salah satu masa tersulit bagi Faker karena meski berhasil membawa timnya ke final Kejuaraan Dunia, mereka harus menerima kekalahan telak dari Samsung Galaxy, yang mengakhiri dominasi SKT T1 di panggung dunia. Di tengah pasang-surut ini, Faker menunjukkan ketangguhan mental dan tetap menjadi andalan timnya. Meski menghadapi kegagalan dalam beberapa turnamen, Faker terus menunjukkan loyalitas dan dedikasi kepada T1, yang menjadi salah satu alasan kuat mengapa ia tetap dihormati oleh komunitas League of Legends.
            </p>
