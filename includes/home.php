            <h3>
                Peran Sebagai Ikon Esports dan Inspirasi Global (2020-Sekarang)
            </h3>
             <p style="font-size: 18px;">
                Meski tidak selalu berada di puncak, Faker tetap menjadi salah satu pemain paling berpengaruh di dunia League of Legends. Ia tidak hanya dikenal sebagai pemain berbakat, tetapi juga sebagai ikon esports global. Dedikasinya kepada tim T1 dan keteguhan mentalnya membuatnya menjadi panutan bagi pemain muda di seluruh dunia. Pada tahun-tahun terakhirnya, Faker juga lebih banyak terlibat dalam menginspirasi generasi pemain baru, baik melalui permainan maupun melalui wawancara yang menekankan pentingnya etos kerja dan dedikasi. Bahkan ketika menghadapi tantangan baru, Faker terus menunjukkan tekad untuk tetap berkompetisi di level tertinggi, membuatnya dihormati tidak hanya sebagai pemain, tetapi juga sebagai legenda hidup di dunia esports.
             </p>
